ROTUSSY_API_KEY=${ROTUSSY_API_KEY:-}
OPENAI_OAUTH_CLIENT_SECRET=${OPENAI_OAUTH_CLIENT_SECRET:-}
GITHUB_OAUTH_CLIENT_ID=${GITHUB_OAUTH_CLIENT_ID:-}
