#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../../../.." && pwd)"
OVERLAY_DIR="$ROOT_DIR/services/proxx/k8s/overlays/local-kind"
SERVICE_DIR="$ROOT_DIR/services/proxx"
KIND_NETWORK="${KIND_NETWORK:-kind}"
GENERATED_DIR="$OVERLAY_DIR/generated"

log() {
  printf '[proxx-k8s] %s\n' "$*"
}

HOST_GATEWAY_IP="$(docker network inspect "$KIND_NETWORK" --format '{{json .IPAM.Config}}' | python3 -c 'import json, sys
configs = json.load(sys.stdin)
for cfg in configs:
    gateway = cfg.get("Gateway")
    if gateway and "." in gateway:
        print(gateway)
        break
else:
    raise SystemExit("No IPv4 gateway found for kind network")')"

ENV_FILE="$SERVICE_DIR/.env"
if [[ -f "$ENV_FILE" ]]; then
  set -a
  # shellcheck disable=SC1090
  source "$ENV_FILE"
  set +a
fi

PROXY_AUTH_TOKEN_VALUE="${PROXY_AUTH_TOKEN:-change-me-open-hax-proxy-token}"
SESSION_SECRET_VALUE="${SESSION_SECRET:-${PROXY_AUTH_TOKEN_VALUE}}"

mkdir -p "$GENERATED_DIR"
cp "$SERVICE_DIR/models.json" "$GENERATED_DIR/models.json"

cat > "$OVERLAY_DIR/config.env" <<EOF
NODE_ENV=production
PROXY_HOST=0.0.0.0
PROXY_PORT=8789
PORT=8789
OPENAI_OAUTH_CALLBACK_PORT=${OPENAI_OAUTH_CALLBACK_PORT:-1455}
PROXY_MODELS_FILE=/app/models.json
PROXY_REQUEST_LOGS_FILE=/app/data/request-logs.jsonl
PROXY_REQUEST_LOGS_MAX_ENTRIES=${PROXY_REQUEST_LOGS_MAX_ENTRIES:-100000}
PROXY_REQUEST_LOGS_FLUSH_MS=${PROXY_REQUEST_LOGS_FLUSH_MS:-1000}
PROXY_PROMPT_AFFINITY_FILE=/app/data/prompt-affinity.json
PROXY_PROMPT_AFFINITY_FLUSH_MS=${PROXY_PROMPT_AFFINITY_FLUSH_MS:-250}
PROXY_SETTINGS_FILE=/app/data/proxy-settings.json
UPSTREAM_PROVIDER_ID=${UPSTREAM_PROVIDER_ID:-openai}
UPSTREAM_FALLBACK_PROVIDER_IDS=${UPSTREAM_FALLBACK_PROVIDER_IDS:-ollama-cloud,rotussy,zai,requesty}
UPSTREAM_BASE_URL=${UPSTREAM_BASE_URL:-https://chatgpt.com/backend-api}
UPSTREAM_PROVIDER_BASE_URLS=${UPSTREAM_PROVIDER_BASE_URLS:-ollama-cloud=https://ollama.com,openai=https://chatgpt.com/backend-api,vivgrid=https://api.vivgrid.com,requesty=https://router.requesty.ai/v1}
OPENAI_PROVIDER_ID=${OPENAI_PROVIDER_ID:-openai}
OPENAI_BASE_URL=${OPENAI_BASE_URL:-https://chatgpt.com/backend-api}
OPENAI_API_BASE_URL=${OPENAI_API_BASE_URL:-https://api.openai.com}
OPENAI_IMAGES_UPSTREAM_MODE=${OPENAI_IMAGES_UPSTREAM_MODE:-auto}
OPENAI_OAUTH_CLIENT_ID=${OPENAI_OAUTH_CLIENT_ID:-app_EMoamEEZ73f0CkXaXp7hrann}
OPENAI_OAUTH_ISSUER=${OPENAI_OAUTH_ISSUER:-https://auth.openai.com}
OPENAI_OAUTH_SCOPES=${OPENAI_OAUTH_SCOPES:-openid profile email offline_access}
OLLAMA_BASE_URL=${OLLAMA_BASE_URL:-http://${HOST_GATEWAY_IP}:11434}
ZAI_PROVIDER_ID=${ZAI_PROVIDER_ID:-zai}
ZAI_BASE_URL=${ZAI_BASE_URL:-https://api.z.ai/api/paas/v4}
ROTUSSY_PROVIDER_ID=${ROTUSSY_PROVIDER_ID:-rotussy}
ROTUSSY_BASE_URL=${ROTUSSY_BASE_URL:-https://api.ussyco.de/v1}
UPSTREAM_CHAT_COMPLETIONS_PATH=${UPSTREAM_CHAT_COMPLETIONS_PATH:-/v1/chat/completions}
OPENAI_CHAT_COMPLETIONS_PATH=${OPENAI_CHAT_COMPLETIONS_PATH:-/codex/responses/compact}
UPSTREAM_MESSAGES_PATH=${UPSTREAM_MESSAGES_PATH:-/v1/messages}
UPSTREAM_MESSAGES_MODEL_PREFIXES=${UPSTREAM_MESSAGES_MODEL_PREFIXES:-claude-}
UPSTREAM_MESSAGES_INTERLEAVED_THINKING_BETA=${UPSTREAM_MESSAGES_INTERLEAVED_THINKING_BETA:-interleaved-thinking-2025-05-14}
UPSTREAM_RESPONSES_PATH=${UPSTREAM_RESPONSES_PATH:-/v1/responses}
OPENAI_RESPONSES_PATH=${OPENAI_RESPONSES_PATH:-/codex/responses}
UPSTREAM_RESPONSES_MODEL_PREFIXES=${UPSTREAM_RESPONSES_MODEL_PREFIXES:-gpt-}
OPENAI_MODEL_PREFIXES=${OPENAI_MODEL_PREFIXES:-openai/,openai:}
OLLAMA_CHAT_PATH=${OLLAMA_CHAT_PATH:-/api/chat}
OLLAMA_MODEL_PREFIXES=${OLLAMA_MODEL_PREFIXES:-ollama/,ollama:}
PROXY_KEY_RELOAD_MS=${PROXY_KEY_RELOAD_MS:-5000}
PROXY_KEY_COOLDOWN_MS=${PROXY_KEY_COOLDOWN_MS:-30000}
UPSTREAM_REQUEST_TIMEOUT_MS=${UPSTREAM_REQUEST_TIMEOUT_MS:-900000}
UPSTREAM_STREAM_BOOTSTRAP_TIMEOUT_MS=${UPSTREAM_STREAM_BOOTSTRAP_TIMEOUT_MS:-900000}
DATABASE_URL=postgresql://openai_proxy:openai_proxy@proxx-postgres:5432/openai_proxy
CHROMA_URL=${CHROMA_URL:-http://${HOST_GATEWAY_IP}:8000}
CHROMA_COLLECTION=${CHROMA_COLLECTION:-open_hax_proxy_sessions}
CHROMA_EMBED_MODEL=${CHROMA_EMBED_MODEL:-nomic-embed-text:latest}
HOST_DASHBOARD_TARGETS_JSON=${HOST_DASHBOARD_TARGETS_JSON:-}
HOST_DASHBOARD_REQUEST_TIMEOUT_MS=${HOST_DASHBOARD_REQUEST_TIMEOUT_MS:-5000}
OTEL_SERVICE_NAME=${OTEL_SERVICE_NAME:-proxx-local-kind}
OTEL_RESOURCE_ATTRIBUTES=${OTEL_RESOURCE_ATTRIBUTES:-service.name=proxx-local-kind}
FEDERATION_SELF_NODE_ID=${FEDERATION_SELF_NODE_ID:-local-primary}
FEDERATION_SELF_GROUP_ID=${FEDERATION_SELF_GROUP_ID:-proxx-local}
FEDERATION_SELF_CLUSTER_ID=${FEDERATION_SELF_CLUSTER_ID:-proxx-local-cluster}
FEDERATION_SELF_PUBLIC_BASE_URL=${FEDERATION_SELF_PUBLIC_BASE_URL:-http://host.docker.internal:8789}
FEDERATION_DEFAULT_OWNER_SUBJECT=${FEDERATION_DEFAULT_OWNER_SUBJECT:-did:web:proxx.local}
FEDERATION_REQUEST_TIMEOUT_MS=${FEDERATION_REQUEST_TIMEOUT_MS:-30000}
EOF

cat > "$OVERLAY_DIR/secret.env" <<EOF
PROXY_AUTH_TOKEN=${PROXY_AUTH_TOKEN_VALUE}
SESSION_SECRET=${SESSION_SECRET_VALUE}
PROXY_TOKEN_PEPPER=${PROXY_TOKEN_PEPPER:-$SESSION_SECRET_VALUE}
REQUESTY_API_TOKEN=${REQUESTY_API_TOKEN:-}
GEMINI_API_KEY=${GEMINI_API_KEY:-}
