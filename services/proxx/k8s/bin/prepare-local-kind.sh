FEDERATION_BRIDGE_DEFAULT_EXECUTION_POLICY=${FEDERATION_BRIDGE_DEFAULT_EXECUTION_POLICY:-node_affinity}
EOF

log "prepared local-kind overlay"
log "host gateway: $HOST_GATEWAY_IP"
log "wrote: $OVERLAY_DIR/config.env"
log "wrote: $OVERLAY_DIR/secret.env"
log "wrote: $GENERATED_DIR/models.json"
